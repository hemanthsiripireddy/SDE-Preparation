package solidPrinciples.S;

public class Book {
	public String name;
	public String author;
	public int year;
   public	String isbn;
   public double price;
	public Book(String name, String author, int year, String isbn, double price) {
		super();
		this.name = name;
		this.author = author;
		this.year = year;
		this.isbn = isbn;
		this.price = price;
	}
	

}
