package entity;

public class ShoppingCart {
	private int id;
	private String name;
	
	public ShoppingCart(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public ShoppingCart() {
		
	}
	

}
